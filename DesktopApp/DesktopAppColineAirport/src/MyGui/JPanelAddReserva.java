    private void btnAddDestinoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddDestinoActionPerformed
        String destino = txtFieldIdDestino.getText();
        if (!Compilador.isInt(destino)) {
            txtAreadConsola.setText(txtAreadConsola.getText() + "No se puede agregar el detino :" + destino + " no es un int valido\n");
        }
        lblDestinos.setText(lblDestinos.getText() + "," + destino);
        txtFieldIdDestino.setText("");
        if (lblDestinos.getText().charAt(0) == ',') {
            lblDestinos.setText(lblDestinos.getText().substring(1));
            
        }
    }//GEN-LAST:event_btnAddDestinoActionPerformed

