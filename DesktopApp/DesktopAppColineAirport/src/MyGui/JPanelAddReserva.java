    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String cliente = txtFieldCliente.getText();
        if(!Compilador.isIdentifier(cliente)){
            txtAreadConsola.setText(txtAreadConsola.getText() + "Cliente no valido debe ser identificador\n");
            return;
        }
        String destinos = lblDestinos.getText();
        if(destinos.split(",").length < 2){
            txtAreadConsola.setText(txtAreadConsola.getText() + "Destinos tiene que tener al menos dos destinos\n");
            return;
        }
        String resultadoWebService = DesktopAppColineAirport.addReserva(cliente, destinos);
        if (resultadoWebService != null) {
            txtAreadConsola.setText(txtAreadConsola.getText() + resultadoWebService + "\n");
        }else{
            lblDestinos.setText("");
            txtFieldCliente.setText("");
        }
    }//GEN-LAST:event_btnAddActionPerformed


